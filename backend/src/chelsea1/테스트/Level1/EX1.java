package chelsea1.테스트.Level1;

public class EX1 {

	public static void main(String[] args) {
		System.out.println(" >> Level1_1 answer >> \n");
		/* 문제풀이 위치 */
		
		// 1.	boolean = true or false 
				boolean a=true;					System.out.println("input  :" + a);
		// 2.	byte 1byte = 8bits
				byte 바이트 = 100; 				System.out.println("byte : " + 바이트 );
		// 3.	char 문자타입 ? 정수타입 ?
				char 차 = 'A';  					System.out.println("char : " + 차);
		// 4. 	short 3만 정도 까지  
				short 쇼트 = 30000; 				System.out.println("short : " + 쇼트);
		// 5.	int 20억 MAX 랄까 
				int 인트 = 2000000000; 			System.out.println("int : " + 인트);
		// 6.	long 20억 이상 ~ 사용
				long 롱 = 4000000000L; 			System.out.println("long : " + 롱);
        // 7.	float 소수점 8자리 
				float 플로트 = 3.1231232F;		System.out.println("float : " + 플로트 );
		// 8.	double 소수점 17자리 까지 표현 
				double 더블  = 3.123123123;		System.out.println("double : " + 더블);
		
		/* -----------  */ 
		
		
		
	} // main end	  
}	// class end



